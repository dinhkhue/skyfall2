     private CTPhatSinh getCtPsSearch() {
        CTPhatSinh ctpsSearch = new CTPhatSinh();
        PhatSinh phatSinh = new PhatSinh();
        phatSinh.setPhieu(tf_phieu.getText());
    //    phatSinh.setHotenKH(tf_hotenkh.getText());
        Kho kho = new Kho();
        kho.setMaKho(tf_makho.getText());
    //    kho.setTenKho(tf_tenkho.getText());
        VatTu vt= new VatTu();
        vt.setMaVT(tf_mavt.getText());
     // vt.setTenVT(tf_tenvt.getText());
        ctpsSearch.setSoluong(Integer.parseInt(tf_soluong.getText()));
        ctpsSearch.setDongia(Float.parseFloat(tf_dongia.getText()));
        ctpsSearch.setLydo(tf_lydo.getText());
        ctpsSearch.setKho(kho);
        ctpsSearch.setPhatsinh(phatSinh);
        ctpsSearch.setVatTu(vt);
        return ctpsSearch;
    }
    

     void loadTable(List<CTPhatSinh> list) {
        tableModel.setRowCount(0);
        for (CTPhatSinh ctps : list) {
            tableModel.addRow(new Object[]{
                ctps.getPhatsinh().getPhieu(),
                ctps.getVatTu().getMaVT(),
                ctps.getSoluong(),
                ctps.getDongia(),
                ctps.getKho().getMaKho(),
                ctps.getLydo(),
            //    ctps.getPhatsinh().getHotenKH(),
                ctps.getVatTu().getTenVT(),
            //    ctps.getKho().getTenKho()
                ctps.getPhatsinh().getNgay()
                
            });
        }
    }

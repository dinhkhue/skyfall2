    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        // TODO add your handling code here:
        try {
            String id = tableModel.getValueAt(tableCTPT.getSelectedRow(), 0).toString();
            int ok = JOptionPane.showConfirmDialog(rootPane, "Xóa  ?");
            if (ok == JOptionPane.YES_OPTION) {
                boolean r = ctpsDao.deleteByPhieu(id);
                if (!r) {
                    JOptionPane.showMessageDialog(rootPane, "Thất bại !");
                } else {
                    loadTable(ctpsDao.findAll());
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Chưa chọn ?");
        }
    }//GEN-LAST:event_xoaActionPerformed

